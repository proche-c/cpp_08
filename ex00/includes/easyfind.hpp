#ifndef EASYFIND_HPP
# define EASYFIND_HPP

#include <algorithm>
#include "NotFoundException.hpp"

template< typename T >
typename T::iterator easyfind(T & c, int value)
{
	typename T::iterator	it;

	it = std::find(c.begin(), c.end(), value);
	if (it == c.end())
		throw NotFoundException();
	return (it);
}

#endif
